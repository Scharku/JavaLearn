package FullLesson;

class Lesson1 { 
    /* 1 урок заключается в обучении вывода информации различными способами 
    через print и println. print просто выдает в строку информацию (без Enter), 
    тогда как println выдает через Enter.
    */
    public static void main(String[] args) {
        System.out.println("Привет, я переношу строку");
        System.out.print("Привет, я не переношу строку");
    }
}